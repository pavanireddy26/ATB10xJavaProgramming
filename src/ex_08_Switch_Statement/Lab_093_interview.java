package ex_08_Switch_Statement;

public class Lab_093_interview {
    public static void main(String[] args) {
        char code = 'C';
        switch (code) {
            default:
                System.out.println("helooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
        }
    }
}