package Practice_Problem_DSA;

public class Practical4_Forloop {
    public static void main(String[] args) {


        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }


        String name[] = {"pabama", "pabana", "jjkk", "jkhkj"};
        for (int i = 0; i < 4; i++) {
            System.out.println(name[i]);
        }

        int numbers[] = {1, 4, 8, 7, 0};
        for (int i = 0; i < 5; i++) {
            System.out.println(numbers[i]);
        }
    }
}
